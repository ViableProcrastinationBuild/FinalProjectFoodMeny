
public class Menu {
String Food;
String Price;
public Menu(String food, String price) {
	this.Food = food;
	this.Price = price;
}
public void setFood(String food) {
	this.Food = food;
}
public String getFood() {
	return Food;
}
public void setPrice(String price ) {
	this.Price = price;
}
public String getPrice() {
	return Price;
	
}
}
