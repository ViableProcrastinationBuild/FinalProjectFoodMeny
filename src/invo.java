
public class invo {
int CheckNum;
int Total;
	public invo( int total,int checkNum) {
		this.CheckNum = checkNum;
		this.Total = total;
	}
	public int getCheckNum() {
		return CheckNum;
	}
	public void setCheckNum(int checkNum) {
		this.CheckNum = checkNum;
	}
	public int getTotal() {
		return Total;
	}
	public void setTotal(int total) {
		this.Total = total;
	}
}
